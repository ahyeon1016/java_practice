package java_1012;

public class ArrayList_add_class {
	private int a;
	private String name;
	
	public ArrayList_add_class() {
	}
	
	public ArrayList_add_class(int a, String name) {
		this.a = a;
		this.name = name;
	}

	
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
