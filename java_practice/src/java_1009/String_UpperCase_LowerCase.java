package java_1009;

public class String_UpperCase_LowerCase {

	public static void main(String[] args) {
		String str1 = "java";
		String str2 = "JAVA";
		
		String str_uperCase = str1.toUpperCase(); 
		String str_lowerCase = str2.toLowerCase();
		
		System.out.println(str_uperCase);
		System.out.println(str_lowerCase);
	}
}
